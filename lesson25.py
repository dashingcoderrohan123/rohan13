#Three cyclists are riding at the speed of 10,20,30 km/h. find the average and compare which cyclist is riding slower than the average speed?

f=10
t=20
g=30

avg=(f+t+g)/3
print(avg)

if f<avg:
    print("First cyclist is driving slowly")

elif t<avg:
    print("second cyclist is driving slowly")

elif g<avg:
    print("third cyclist is driving slowly")
            

